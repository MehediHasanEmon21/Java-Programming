
package javacode;


public class DataType {

    
    public static void main(String[] args) {
        boolean b;
        int i;
        char c;
        float f;
        double d;
        
        b = true;
        i = 10;
        f = 2.3f;
        
        System.out.printf("b = %b\n",b);
        System.out.printf("i = %d\n",i);
        System.out.printf("f = %.2f\n",f);
        
    }
    
}
